import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are a Natural Disaster Safety Expert. Provide CONCISE, ACTIONABLE safety advice.

FORMATTING RULES:
- Use markdown for formatting (** for bold, * for bullets)
- Keep answers SHORT (3-6 sentences or 4-6 bullet points max)
- Start with most critical action first
- Use clear sections with line breaks

IMMEDIATE DANGER RESPONSE:
When someone is IN danger NOW, format like this:

**⚠️ IMMEDIATE ACTION:**
* Go to lowest interior room (bathroom, closet)
* Stay away from windows
* Get under sturdy furniture, protect your head
* If outside, lie flat in a ditch

**Call 911 for emergencies.**

TOPICS YOU COVER:
Disaster preparation, during disasters (earthquakes, tornadoes, hurricanes, wildfires, floods), first aid basics, survival essentials, post-disaster safety.

STYLE:
- Direct and calm tone
- Use bold (**text**) for key actions and warnings
- Use bullet lists (* item) for steps
- 4-6 bullet points maximum
- Brief and actionable`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
